package com.tipos;

public class OperacoesMatematicas {

	public static void main(String[] args) {
		int numeroa = 12;
		int numerob = 24;

		System.out.println(numeroa + numerob);
		System.out.println(numeroa / numerob);
		System.out.println(numeroa - numerob);
		System.out.println(numeroa * numerob);
		System.out.println(numeroa % numerob);

	}

}
